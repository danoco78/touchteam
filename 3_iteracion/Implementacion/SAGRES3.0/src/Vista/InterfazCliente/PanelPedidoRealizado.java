    public int actualizar() {
        // Comprobamos si ha cambiado algun pedido
        ArrayList<Pedido> pedidos = this.panelGeneralCliente.icliente.obtienePedidosMesa(codMesa);
        if(pedidos == null){
            if(pedidosMostrandose != null)
                this.pedidosMostrandose.clear();
            this.PanelPedido.removeAll();
            this.panelGeneralCliente.volverAlInicio();
        }else if(hayQueActualizar(pedidosMostrandose, pedidos)) {
            if(pedidosMostrandose != null)
                this.pedidosMostrandose.clear();
            this.pedidosMostrandose = this.copiarPedido(pedidos);

            // Si ha cambiado, repintamos
            this.PanelPedido.removeAll();

            Iterator itPedidos = pedidos.iterator();
            boolean todosPagados = true;
            while (itPedidos.hasNext()) {
                Pedido pedido = (Pedido) itPedidos.next();
                ArrayList<ElementoPedido> listaElementosPedido = pedido.obtieneElementos();

                ArrayList<Elemento> listaElementos = new ArrayList();
                Iterator itElementos = listaElementosPedido.iterator();
                while (itElementos.hasNext()) {
                    ElementoPedido elementoPedido = (ElementoPedido) itElementos.next();
                    listaElementos.add(elementoPedido.getElemento());
                }

                if (pedido.getEstado() != Pedido.FACTURADO) {
                    ArrayList<ElementoPedido> elementos = pedido.getElementos();
                    Iterator<ElementoPedido> it = elementos.iterator();
                    boolean mostrar = false;
                    // Iteramos sobre todos los elementos, si todos han sido preparados...
                    // ...no lo mostramos en la interfaz
                    while (it.hasNext() && !mostrar) {
                        ElementoPedido ep = it.next();
                        if (ep instanceof ElementoColaBar) {
                            if (ep.getEstado() != ElementoColaBar.PREPARADO) {
                                mostrar = true;
                            }
                        } else if (ep instanceof ElementoColaCocina) {
                            if (ep.getEstado() != ElementoColaCocina.PREPARADO) {
                                mostrar = true;
                            }
                        }
                    }
                    if (mostrar) {
                        this.anadirPedido(listaElementos, pedido.getCodPedido(), pedido.getEstado());
                    }
                    todosPagados = false;
                }
                //this.panelGeneralCliente.eliminarPedido(pedido.getCodPedido(), false);
            }

            if (todosPagados == true) {
                this.panelGeneralCliente.volverAlInicio();
            } else {
                if (pedidos.size() == 0) {
                    this.BotonVerFactura.setEnabled(false);
                } else {
                    this.BotonVerFactura.setEnabled(true);
                }

                this.PanelPedido.repaint();
                this.PanelPedido.revalidate();
            }
        } else{
            System.gc();
        }
        
        return pedidos.size();
    }

    /**
     * Comprueba si es necesario actualizar
     * @param pedMostrandose Vector de &gt;CodPedido, EstadoPedido, CodElem, EstadoElem...&lt;
     * @param ped
     * @return
     */
    private boolean hayQueActualizar(Vector<Vector<Integer> > pedsMostrandose, ArrayList<Pedido> peds) {
        if((pedsMostrandose == null && peds != null) ||
                    (pedsMostrandose != null && peds == null))
                return true;
        if(pedsMostrandose == null && peds == null) return false;
        if((peds.isEmpty() && !pedsMostrandose.isEmpty()) ||
                (!peds.isEmpty() && pedsMostrandose.isEmpty())){
            return true;
        }
        if(peds.isEmpty() && pedsMostrandose.isEmpty()) return false;

        if(pedsMostrandose.size() != peds.size()) return true;
        Iterator<Vector<Integer> > itMostrandose = pedsMostrandose.iterator();
        while(itMostrandose.hasNext()){
            Vector<Integer> pedMostrandose = itMostrandose.next();
            Iterator<Pedido> itPeds = peds.iterator();
            Pedido ped = null;
            // Buscamos el pedido dentro de nuestra lista de pedidos mostrandose
            boolean encontrado = false;
            while(itPeds.hasNext() && !encontrado){
                ped = itPeds.next();
                if(ped.getCodPedido() == pedMostrandose.get(0)){
                    encontrado = true;
                }
            }
            if(!encontrado) return true;
            if(pedMostrandose.get(1) != ped.getEstado()) return true;
            ArrayList<ElementoPedido> elementos = ped.getElementos();
            // No se puede comparar los tama√±os porque pedMostrandose solo guarda los que corresponde al filtro
            //if((pedMostrandose.size()-2)/2 != elementos.size()) return true;

            // Buscamos la correspondencia entre elementos
            Iterator<ElementoPedido> iterator = elementos.iterator();
            while(iterator.hasNext()){
                ElementoPedido elem = iterator.next();
                if(elem instanceof ElementoColaCocina){
                    encontrado = false;
                    for(int i=2; i< pedMostrandose.size() && !encontrado; i+=2){
                        if(pedMostrandose.get(i) == elem.getCodElementoPedido()){
                            encontrado = true;
                            if(pedMostrandose.get(i+1) != elem.getEstado()){
                                return true;
                            }
                        }
                    }
                }
            }
        }

        return false;
    }
    
    /**
     * Copia una lista de pedidos y lo devuelve en forma de vector de codigos y estados.
     * @param peds Pedidos a copiar
     * @return Nuevo pedido sin referencias a peds
     */
    private Vector<Vector<Integer> > copiarPedido(ArrayList<Pedido> peds){

        Vector<Vector<Integer> > copiaPeds = new Vector(peds.size());
        Iterator<Pedido> itPeds = peds.iterator();

        while(itPeds.hasNext()){
            Pedido ped = itPeds.next();
            Vector<Integer> copia = new Vector<Integer>(ped.getElementos().size()*2 +2);
            copia.add(ped.getCodPedido());
            copia.add(ped.getEstado());

            Iterator<ElementoPedido> it = ped.getElementos().iterator();
            while(it.hasNext()){
                ElementoPedido next = it.next();
                if(next instanceof ElementoColaCocina){
                    copia.add(next.getCodElementoPedido());
                    copia.add(next.getEstado());
                }
            }
            copiaPeds.add(copia);
        }
        return copiaPeds;
    }

